                <h1>ETAPA 5</h1>
                <h3>Inscripción</h3>
                <h1>Del 16 al 20 de agosto 2021</h1>
            </div>
        </div>
        <p>La inscripción de los aspirantes aceptados se realizará en las instalaciones de la facultad
            sede del programa de posgrado. Asimismo se deberá cumplir con los requisitos específicos
            establecidos por el programa de posgrado. Esto incluye fechas específicas para realizar el
            proceso de inscripción dentro del período señalado, por lo que deberá consultarse los días y
            horarios que programe la facultad sede correspondiente para realizar dicha inscripción.</p>
        <p>Documentación a entregar a control escolar al momento de la inscripción:</p>
        <ol>
            <li>Original del certificado de estudios completos de licenciatura o maestría, según el nivel
                del programa a cursar.</li>
            <li>Original del título de licenciatura o grado de maestría, según el nivel del programa a cursar.</li>
            <li>Fotocopia de la cédula profesional de licenciatura o maestría.</li>
            <li>Original del acta de nacimiento.</li>
            <li>Fotocopia de la CURP</li>
            <li>Número de Seguridad Social.</li>
        </ol>
        <p>Notas:</p>
        <ol>
            <li>En caso de tener todos sus documentos con originales en electrónico (NO documentos escaneados),
                podrán realizar su inscripción totalmente en línea ingresando al sitio:
                <a href="http://www.sicei.uady.mx/siceiweb" target="_blank">www.sicei.uady.mx/siceiweb</a>.
            </li>
            <li>En caso de aspirantes aceptados que hayan estudiado en el extranjero, deberán traer el oficio
                de la revalidación o el dictamen técnico emitido por la SEP del certificado de estudios de
                licenciatura o maestría, según sea el caso. De no contar con dicho documento se deberá presentar
                una constancia de que está en trámite.</li>
            <li>Después del proceso de indexación de documentos, se devuelven los originales mediante una
                constancia oficial de la facultad donde conste la entrega de los mismos y la firma del interesado.</li>
            <li>Si el título, grado se encuentra en trámite, se deberá presentar una constancia oficial con sello
                de la institución donde cursó la licenciatura o maestría, que avale dicho trámite.</li>
            <li>Para las universidades que consideran la graduación de sus estudiantes a través de cursar un semestre
                de estudio en un PEP, los estudiantes tendrán 8 meses a partir del inicio del curso para entregar
                su título o constancia de que éste se encuentra en trámite.</li>
        </ol>
