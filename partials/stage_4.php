        <p>Las listas de los aspirantes aceptados a ingresar en los programas de posgrado de 
            la UADY se publicarán en el sitio del proceso en la fecha indicada.</p>        
        <p>A partir de la publicación de los resultados, cada aspirante podrá solicitar al coordinador 
            del programa el dictamen con el resultado del proceso de selección, en el que se debe 
            indicar claramente si el aspirante fue o no aceptado, así como las razones que sustentan 
            la decisión.</p>
        <p>Los resultados del proceso de selección son validados por el cuerpo colegiado (comité académico 
            o similar) y no serán reconsiderados, excepto que se detecte algún error de procedimiento, 
            en cuyo caso deberá comunicarse a la Coordinación General de Posgrado, Investigación y 
            Vinculación al teléfono +52(999)9300100 ext. 47115 en horario de 8:00 a 14:00 de lunes a 
            viernes o al siguiente correo <a href="mailto:posgrados@correo.uady.mx">posgrados@correo.uady.mx</a></p>
