        <p>Para imprimir el comprobante de registro deberás:</p>
        <ol>
            <li>Acceder al SIPS con tu clave de usuario y contraseña.</li>
            <li>Seleccionar la opción “Descargar comprobante de registro”.</li>
        </ol>
        <p>En el comprobante se indicará:</p>
        <ol>
            <li>Lugar y horario donde se presenta el EXANI III.</li>
            <li>Materiales de apoyo permitidos en el examen.</li>
            <li>Documentos indispensables para presentar el examen.</li>
        </ol>
        <p>Para los aspirantes que presentarán el EXANI III en la fecha que aplica la UADY, este
            comprobante será el pase de ingreso al examen. El comprobante solo lo podrán imprimir los
            aspirantes que hayan completado satisfactoriamente la etapa 1 del proceso y hayan pagado
            la cuota de derechos.</p>
        <p>Es importante verificar que estén correctos todos sus datos ya que no podrán ingresar al
            examen si no concuerdan con la identificación presentada.</p>
        <p>Los aspirantes que cuentan con EXANI III vigente deberán comunicarse con el coordinador
            del PEP para entregar el resultado del examen.</p>
