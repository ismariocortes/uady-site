        <p>Si eres estudiante extranjero, deberás cumplir con los requisitos y procedimientos
            que se especifican en el sitio web www.selección.uady.mx, opción “Posgrado”.</p>
        <p>Los aspirantes deberán contar con la certificación del idioma inglés de acuerdo con
            las equivalencias correspondientes (<a href="index.php#features">ver anexo de equivalencias</a>
