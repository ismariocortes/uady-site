        <p>Para que un aspirante se considere completa y correctamente registrado al proceso,
            deberá realizar en su totalidad las actividades de las Etapas 1 y 2 de esta convocatoria.</p>
        <p>Para completar la Etapa 1, Registro en línea, los aspirantes deberán ingresar al
            Sistema de Información para el Proceso de Selección (SIPS), y ejecutar los siguientes pasos:</p>
        <ol>
            <li>Generar clave de usuario y contraseña.
                <ul>
                    <li>Ingresar al sitio <a href="http://www.seleccion.uady.mx/posgrado" target="_blank">www.seleccion.uady.mx/posgrado</a>.</li>
                    <li>Seleccionar la opción "Acceder al SIPS”.</li>
                    <li>Generar tu clave de usuario y contraseña, mismos que te servirán para
                        ingresar al SIPS cada vez que lo requieras.</li>
                    <li>Seguir el procedimiento indicado para proporcionar la información que se te solicita.</li>
                    <li>Elegir el programa de tu interés. NOTA: La elección del programa de posgrado NO podrá cambiarse posteriormente.</li>
                </ul>
            </li>
            <li>Completar el registro en línea.
                <ul>
                    <li>Ingresar al sitio <a href="http://www.seleccion.uady.mx/posgrado" target="_blank">www.seleccion.uady.mx/posgrado</a>.</li>
                    <li>Seleccionar la opción “Acceder al SIPS”.</li>
                    <li>Introducir tu clave de usuario y contraseña y seguir el procedimiento indicado
                        para proporcionar la información que se te solicita.</li>
                    <li>Responder la encuesta CENEVAL.</li>
                    <li>Subir fotografía.</li>
                </ul>
            </li>
            <li>Realizar el pago de la cuota de recuperación (en ventanilla bancaria o en línea). <br>
                Después de validar tu fotografía, el SIPS te llevará a la sección de pago de la cuota de recuperación.
                <ul>
                    <li>Si eliges realizar tu pago en ese momento, deberás elegir la opción “Pago en línea“
                        y podrás pagar con cualquier tarjeta de crédito bancaria o con tarjeta de débito
                        de cualquier banco.</li>
                    <li>Si eliges efectuar tu pago posteriormente, deberás elegir la opción “Pago en ventanilla”
                        y visitar cualquier sucursal de Bancomer.</li>
                </ul>
            </li>
        </ol>
        <p>Para el pago de la cuota de recuperación, deberás seleccionar una de las opciones siguientes:</p>
        <ol>
            <li>Pago en línea con cualquier tarjeta de débito o crédito bancaria.</li>
            <li>Pago en ventanilla en sucursal de Bancomer.</li>
        </ol>
